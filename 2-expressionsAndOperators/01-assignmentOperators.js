// ASSIGNMENT OPERATOR
let r = 5;

// ADDITION ASSIGNMENT

r += 1; // r = r + 1

// SUBTRACTION ASSIGNMENT

r -= 1; // r = r - 1

// MULTIPLICATION ASSIGNMENT

r *= 2; // r = r * 2

// DIVISION ASSIGNMENT

r /= 2; // r = r / 2

// REMAINDER (or modulus) ASSIGNMENT

r %= 1; // r = r % 1

// EXPONENTIAL ASSIGNMENT

r **= 1; // r = r ^ 1